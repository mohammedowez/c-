
struct Node {
    int data;
    struct Node *next;
    Node(int x)
    {
        data = x;
        next = NULL;
    }
};




class Solution
{
    public:
    //Function to reverse a linked list.
     Node* reverseList( Node *head)
    {
        // code here
        // return head of reversed list
         Node *curr,*prev,*next;
        curr=head;
        prev=NULL;
        while(curr!=NULL)
        {
            next=curr->next;
            curr->next=prev;
            prev=curr;
            curr=next;
        }
        return prev;
    }
};
    


